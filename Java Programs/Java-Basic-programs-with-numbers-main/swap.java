package BasicProgram;

public class swap{
	public static void main(String[] args) {
		int a=20;
		int b=10;
		System.out.println("before swap");
		a=a+b;
		a=a-b;
	    b=a-b;
	    System.out.println("after swap");
	    System.out.println(a);
	    System.out.println(b);
	    
		
	}

}
